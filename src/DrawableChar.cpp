#include "tiage/DrawableChar.h"

namespace tiage {

// --------------------------------------------------------------------------------------------------

const DrawableChar kDefault = { ' ' , Color::kWhite, Color::kBlack };

// --------------------------------------------------------------------------------------------------

DrawableChar::DrawableChar(char c, const Color& fg, const Color& bg) :
	c_(c),
	fg_(fg),
	bg_(bg) {
}

// --------------------------------------------------------------------------------------------------

bool 
DrawableChar::operator== (const DrawableChar& other) const {
	return	this->c_ == other.c_ ||
			this->fg_ == other.fg_ || 
			this->bg_ == other.bg_;
}

// --------------------------------------------------------------------------------------------------

void
DrawableChar::setC(char c) {
	c_ = c;
}

// --------------------------------------------------------------------------------------------------

void
DrawableChar::setFG(const Color& fg) {
	fg_ = fg;
}

// --------------------------------------------------------------------------------------------------

void
DrawableChar::setBG(const Color& bg) {
	bg_ = bg;
}

// --------------------------------------------------------------------------------------------------

char
DrawableChar::c() const {
	return c_;
}

// --------------------------------------------------------------------------------------------------

Color
DrawableChar::bg() const {
	return bg_;
}

// --------------------------------------------------------------------------------------------------

Color
DrawableChar::fg() const {
	return fg_;
}

// --------------------------------------------------------------------------------------------------

} // tiage
